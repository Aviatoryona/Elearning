package com.aviator.elearning.el.models;

import android.os.Parcel;
import android.os.Parcelable;

@SuppressWarnings("ALL")
public class UserModel implements Parcelable {
    private int user_id;
    private String user_name, user_email, user_contact, user_password, user_passwordsalt,
            user_type, user_image, user_dateadded, user_status, country;

    public UserModel() {
    }

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getUser_email() {
        return user_email;
    }

    public void setUser_email(String user_email) {
        this.user_email = user_email;
    }

    public String getUser_contact() {
        return user_contact;
    }

    public void setUser_contact(String user_contact) {
        this.user_contact = user_contact;
    }

    public String getUser_password() {
        return user_password;
    }

    public void setUser_password(String user_password) {
        this.user_password = user_password;
    }

    public String getUser_passwordsalt() {
        return user_passwordsalt;
    }

    public void setUser_passwordsalt(String user_passwordsalt) {
        this.user_passwordsalt = user_passwordsalt;
    }

    public String getUser_type() {
        return user_type;
    }

    public void setUser_type(String user_type) {
        this.user_type = user_type;
    }

    public String getUser_image() {
        return user_image;
    }

    public void setUser_image(String user_image) {
        this.user_image = user_image;
    }

    public String getUser_dateadded() {
        return user_dateadded;
    }

    public void setUser_dateadded(String user_dateadded) {
        this.user_dateadded = user_dateadded;
    }

    public String getUser_status() {
        return user_status;
    }

    public void setUser_status(String user_status) {
        this.user_status = user_status;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    @Override
    public int describeContents() {
        return 0;
    }

    @Override
    public void writeToParcel(Parcel dest, int flags) {

    }
}
